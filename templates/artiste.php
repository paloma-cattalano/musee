      <p class="blabla">Personnalité hors-norme, première femme d’affaires du XXe siècle, self-made-woman affranchie et visionnaire… Si les superlatifs fusent pour décrire l’ascension vertigineuse d’Helena Rubinstein (1870-1965), l’Impératrice de la beauté selon Cocteau, on en oublie parfois son parcours de collectionneuse aguerrie et son rôle pionnier dans la reconnaissance des arts africains et océaniens en Europe et outre-Atlantique. Construite essentiellement à Paris, au gré de ses rencontres, « la collection de Madame », aujourd’hui dispersée, rassemblait plus de 400 pièces d’art extra-européen, précieux gardiens de reliquaires kota ou fang, pièces d’exception baoulé, bamana, senoufo ou dogon, qui voisinaient avec les œuvres de peintres et sculpteurs de la modernité, Chagall, Braque ou Picasso, et beaucoup d’autres domaines de collection.
      À travers une soixantaine de pièces, l’exposition met à l’honneur sa passion pour les arts extra-occidentaux, principalement l’art africain, et sa fascination pour leur intensité expressive et leur caractère. Des arts qu’elle a découverts dans les années 1910, au contact du sculpteur Jacob Epstein puis de collectionneurs d’avant-garde parisiens, et qu’elle mettra un point d’honneur à mettre en valeur dans ses intérieurs de Paris, New-York et Londres mais aussi, dans un souci d’éducation de sa clientèle féminine, dans ses salons de beauté du monde entier et en participant à de grandes expositions.
